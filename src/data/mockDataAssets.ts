// Função para gerar ativos para cada centro
function generateAssetsForCenter(centerCode: string, centerName: string, phases: string[]): Asset[] {
  const assets: Asset[] = [];
  let globalCounter = 1;

  phases.forEach(phase => {
    const equipmentTypes = getEquipmentTypesForPhase(phase);
    let phaseAssetCount = 0;
    
    for (const equipType of equipmentTypes) {
      const count = Math.floor(Math.random() * 6) + 5;
      for (let i = 1; i <= count && phaseAssetCount < 50; i++) {
        const id = `${centerCode}-${globalCounter.toString().padStart(3, '0')}`;
        const tag = `${centerCode}-${equipType.prefix}-${i.toString().padStart(3, '0')}`;
        const name = `${equipType.name} ${i.toString().padStart(3, '0')}`;
        
        assets.push({
          id,
          tag,
          name,
          type: equipType.type,
          locationCenter: centerCode,
          phase: phase as any
        });
        
        globalCounter++;
        phaseAssetCount++;
      }
      if (phaseAssetCount >= 50) break;
    }
    
    // Completar com equipamentos genéricos se necessário
    while (phaseAssetCount < 50) {
      const id = `${centerCode}-${globalCounter.toString().padStart(3, '0')}`;
      const tag = `${centerCode}-GEN-${phaseAssetCount.toString().padStart(3, '0')}`;
      const name = `Equipamento Genérico ${phaseAssetCount.toString().padStart(3, '0')}`;
      
      assets.push({
        id,
        tag,
        name,
        type: "Equipamento",
        locationCenter: centerCode,
        phase: phase as any
      });
      
      globalCounter++;
      phaseAssetCount++;
    }
  });

  return assets;
}

